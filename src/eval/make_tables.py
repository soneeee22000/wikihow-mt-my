"""Generate the LaTeX main-results table from experiments/results/main_results.json.

Keeps the paper's numbers traceable to JSON (no hand-typed metrics). Writes
paper/tables/main_results.tex.
"""
import json
import os

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
RES = os.path.join(ROOT, "experiments", "results", "main_results.json")
OUT = os.path.join(ROOT, "paper", "tables", "main_results.tex")

# display order + pretty names
ORDER = [
    ("nllb_zeroshot", "NLLB-200-distilled-600M (zero-shot)"),
    ("nllb_finetuned", "NLLB-200-distilled-600M (fine-tuned)"),
    ("gemini_fewshot", "Gemini 2.5 (5-shot)"),
    ("google_translate", "Google Translate"),
]


def fmt(v):
    return "--" if v is None else f"{v:.2f}"


def main():
    data = json.load(open(RES, encoding="utf-8")) if os.path.exists(RES) else {}
    rows = []
    for key, name in ORDER:
        r = data.get(key)
        if not r:
            continue
        rows.append(f"{name} & {fmt(r.get('chrf++'))} & {fmt(r.get('spbleu'))} "
                    f"& {fmt(r.get('bleu'))} & {fmt(r.get('comet'))} \\\\")
    body = "\n".join(rows) if rows else "\\multicolumn{5}{c}{(no results yet)} \\\\"

    tex = (
        "% auto-generated by src/eval/make_tables.py -- do not edit by hand\n"
        "\\begin{table}[t]\n\\centering\n\\small\n"
        "\\begin{tabular}{lrrrr}\n\\toprule\n"
        "System & chrF++ & spBLEU & BLEU & COMET \\\\\n\\midrule\n"
        f"{body}\n\\bottomrule\n\\end{{tabular}}\n"
        "\\caption{English$\\rightarrow$Myanmar results on the WikiHow-MY test set "
        "(article-disjoint, $n$=846). chrF++ is the primary metric; spBLEU uses the "
        "FLORES-200 tokenizer.}\n\\label{tab:main}\n\\end{table}\n"
    )
    os.makedirs(os.path.dirname(OUT), exist_ok=True)
    with open(OUT, "w", encoding="utf-8") as f:
        f.write(tex)
    print(tex)
    print(f"-> {OUT}")


if __name__ == "__main__":
    main()
