"""Generate the paper's LaTeX tables from experiments/results/*.json.

Keeps every number traceable to JSON (no hand-typed metrics). Writes:
  paper/tables/main_results.tex     -- chrF++/spBLEU/BLEU/COMET/IFS per system
  paper/tables/ifs_components.tex   -- IFS overall + components per system
  paper/tables/ifs_correlation.tex  -- IFS vs human correlation (only if available)

Reads main_results.json, ifs_results.json and (optionally) ifs_correlation.json.
"""
import json
import os

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
RES = os.path.join(ROOT, "experiments", "results")
TAB = os.path.join(ROOT, "paper", "tables")
MAIN = os.path.join(RES, "main_results.json")
IFS = os.path.join(RES, "ifs_results.json")
CORR = os.path.join(RES, "ifs_correlation.json")
RERANK = os.path.join(RES, "rerank_report.json")

# system key -> display name; also fixes the gemini/gtranslate naming used by llm_baselines.py
ORDER = [
    ("nllb_zeroshot", "NLLB-200-distilled-600M (zero-shot)"),
    ("nllb_finetuned", "NLLB-200-distilled-600M (fine-tuned)"),
    ("gemini", "Gemini 2.5 (5-shot)"),
    ("gtranslate", "Google Translate"),
]
METRIC_NAMES = {"ifs": "IFS", "chrf": "chrF++", "bleu": "BLEU", "comet": "COMET"}


def load(path: str) -> dict:
    """Load a results JSON, or {} if it doesn't exist yet."""
    return json.load(open(path, encoding="utf-8")) if os.path.exists(path) else {}


def fmt(v) -> str:
    """Two-decimal LaTeX cell, or en-dash for missing values."""
    return "--" if v is None else f"{v:.2f}"


def write(path: str, tex: str) -> None:
    """Write a .tex file, creating the tables dir."""
    os.makedirs(TAB, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(tex)
    print(f"-> {path}")


def main_table(main: dict, ifsd: dict) -> str:
    """chrF++/spBLEU/BLEU/COMET/IFS per system (primary results table)."""
    rows = []
    for key, name in ORDER:
        r = main.get(key)
        if not r:
            continue
        ifs_v = ifsd.get(key, {}).get("ifs")
        rows.append(f"{name} & {fmt(r.get('chrf++'))} & {fmt(r.get('spbleu'))} & "
                    f"{fmt(r.get('bleu'))} & {fmt(r.get('comet'))} & "
                    f"{fmt(r.get('metricx24'))} & {fmt(ifs_v)} \\\\")
    body = "\n".join(rows) if rows else "\\multicolumn{7}{c}{(no results yet)} \\\\"
    return (
        "% auto-generated by src/eval/make_tables.py -- do not edit by hand\n"
        "\\begin{table}[t]\n\\centering\n\\small\n\\begin{tabular}{lrrrrrr}\n\\toprule\n"
        "System & chrF++ & spBLEU & BLEU & COMET & MetricX$\\downarrow$ & IFS \\\\\n\\midrule\n"
        f"{body}\n\\bottomrule\n\\end{{tabular}}\n"
        "\\caption{English$\\rightarrow$Myanmar results on the WikiHow-MY test set "
        "(article-disjoint, $n$=846). chrF++ is the primary metric; spBLEU uses the "
        "FLORES-200 tokenizer; MetricX-24 (hybrid-XL, reference-based) is an error score "
        "in $[0,25]$ where lower is better ($\\downarrow$); IFS is the Instruction "
        "Faithfulness Score.}\n"
        "\\label{tab:main}\n\\end{table}\n"
    )


def ifs_table(ifsd: dict) -> str:
    """IFS overall + automatic components per system."""
    rows = []
    for key, name in ORDER:
        r = ifsd.get(key)
        if not r:
            continue
        rows.append(f"{name} & {fmt(r.get('ifs'))} & {fmt(r.get('ifs_step'))} & "
                    f"{fmt(r.get('ifs_entity'))} & {fmt(r.get('ifs_quantity'))} \\\\")
    body = "\n".join(rows) if rows else "\\multicolumn{5}{c}{(no IFS results yet)} \\\\"
    return (
        "% auto-generated by src/eval/make_tables.py -- do not edit by hand\n"
        "\\begin{table}[t]\n\\centering\n\\small\n\\begin{tabular}{lrrrr}\n\\toprule\n"
        "System & IFS & step & entity & quantity \\\\\n\\midrule\n"
        f"{body}\n\\bottomrule\n\\end{{tabular}}\n"
        "\\caption{IFS overall and automatic components (action faithfulness is "
        "assessed via the human protocol). Higher is more source-faithful; note that "
        "literal MT can exceed the human reference on surface preservation.}\n"
        "\\label{tab:ifs}\n\\end{table}\n"
    )


def corr_table(corr: dict) -> str:
    """Metric-vs-human-followability correlations with Williams test vs IFS."""
    corrs = corr.get("correlations", {})
    williams = corr.get("williams_ifs_vs", {})
    rows = []
    for metric in ("ifs", "comet", "chrf", "bleu"):
        c = corrs.get(metric)
        if not c:
            continue
        w = williams.get(f"ifs_vs_{metric}", {})
        wp = "--" if metric == "ifs" else fmt(w.get("p"))
        rows.append(f"{METRIC_NAMES[metric]} & {fmt(c.get('pearson'))} & "
                    f"{fmt(c.get('spearman'))} & {wp} \\\\")
    body = "\n".join(rows)
    n = corr.get("n", "?")
    return (
        "% auto-generated by src/eval/make_tables.py -- do not edit by hand\n"
        "\\begin{table}[t]\n\\centering\n\\small\n\\begin{tabular}{lrrr}\n\\toprule\n"
        "Metric & Pearson $r$ & Spearman $\\rho$ & Williams $p$ (vs IFS) \\\\\n\\midrule\n"
        f"{body}\n\\bottomrule\n\\end{{tabular}}\n"
        "\\caption{Segment-level correlation with human instruction-followability "
        f"($n$={n}). Williams $p$ tests whether IFS's correlation differs from each "
        "metric's.}\n\\label{tab:corr}\n\\end{table}\n"
    )


def flores_table(main: dict) -> str:
    """In-domain (WikiHow) vs out-of-domain (FLORES+) for the two NLLB systems."""
    pairs = [
        ("NLLB (zero-shot)", "nllb_zeroshot", "nllb_zeroshot_flores"),
        ("NLLB (fine-tuned)", "nllb_finetuned", "nllb_finetuned_flores"),
        ("Google Translate", "gtranslate", "gtranslate_flores"),
        ("Gemini 2.5 (5-shot)", "gemini", "gemini_flores"),
    ]
    rows = []
    for name, wk, fl in pairs:
        w, f = main.get(wk), main.get(fl)
        if not (w and f):
            continue
        rows.append(f"{name} & {fmt(w.get('chrf++'))} & {fmt(w.get('comet'))} & "
                    f"{fmt(w.get('metricx24'))} & {fmt(f.get('chrf++'))} & "
                    f"{fmt(f.get('comet'))} & {fmt(f.get('metricx24'))} \\\\")
    body = "\n".join(rows) if rows else "\\multicolumn{7}{c}{(no FLORES+ results yet)} \\\\"
    return (
        "% auto-generated by src/eval/make_tables.py -- do not edit by hand\n"
        "\\begin{table}[t]\n\\centering\n\\small\n\\begin{tabular}{lrrrrrr}\n\\toprule\n"
        " & \\multicolumn{3}{c}{WikiHow-MY ($n$=846)} & "
        "\\multicolumn{3}{c}{FLORES+ ($n$=1012)} \\\\\n"
        "\\cmidrule(lr){2-4}\\cmidrule(lr){5-7}\n"
        "System & chrF++ & COMET & MetricX$\\downarrow$ & chrF++ & COMET & MetricX$\\downarrow$ \\\\\n"
        "\\midrule\n"
        f"{body}\n\\bottomrule\n\\end{{tabular}}\n"
        "\\caption{Domain generalization: in-domain WikiHow-MY test vs.\\ out-of-domain "
        "FLORES+ devtest (eng\\_Latn$\\rightarrow$mya\\_Mymr). MetricX-24 is an error score "
        "($\\downarrow$ lower is better). Fine-tuning on WikiHow improves both domains, "
        "with no degradation on general-domain FLORES+.}\n"
        "\\label{tab:flores}\n\\end{table}\n"
    )


def rerank_table(rr: dict, main: dict) -> str:
    """N-best reranking ablation on test: reference-free selectors vs the oracle ceiling."""
    v = rr.get("variants_test", {}).get("chrf", {})
    diag = rr.get("diagnostics_test", {})
    alpha = rr.get("tuning", {}).get("best_alpha")
    beam1 = v.get("beam1")
    gt = main.get("gtranslate", {}).get("chrf++")

    def delta(x) -> str:
        return "--" if (x is None or beam1 is None) else f"{x - beam1:+.2f}"

    order = [
        ("beam1", "Top beam (baseline)"),
        ("qe", "\\;+ QE rerank (COMET-Kiwi)"),
        ("ifs", "\\;+ IFS rerank"),
        ("fused", f"\\;+ fused QE\\,+\\,IFS ($\\alpha{{=}}{alpha}$)"),
        ("mbr", "\\;+ MBR (chrF++ consensus)"),
    ]
    rows = [f"{name} & {fmt(v.get(k))} & {delta(v.get(k))} \\\\" for k, name in order]
    rows.append("\\midrule")
    rows.append(f"Oracle (reference chrF++; ceiling) & {fmt(v.get('oracle'))} & "
                f"{delta(v.get('oracle'))} \\\\")
    rows.append(f"Google Translate (target) & {fmt(gt)} & {delta(gt)} \\\\")
    body = "\n".join(rows)
    sp = diag.get("mean_qe_vs_refchrf_spearman")
    agree = diag.get("qe_oracle_agreement_pct")
    rand = diag.get("random_agreement_pct")
    headroom = diag.get("headroom_in_nontop_beams_pct")
    return (
        "% auto-generated by src/eval/make_tables.py -- do not edit by hand\n"
        "\\begin{table}[t]\n\\centering\n\\small\n\\begin{tabular}{lrr}\n\\toprule\n"
        "Selector (fine-tuned NLLB 16-best) & chrF++ & $\\Delta$ \\\\\n\\midrule\n"
        f"{body}\n\\bottomrule\n\\end{{tabular}}\n"
        "\\caption{Decoding-time reranking of the fine-tuned NLLB's 16-best on the "
        "WikiHow-MY test set ($n$=846), $\\Delta$ vs.\\ the top beam. A reference-optimal "
        "selection exists that beats Google Translate (oracle "
        f"{fmt(v.get('oracle'))}~$>$~{fmt(gt)}), but no reference-free selector recovers it: "
        f"COMET-Kiwi's per-source ranking correlates only $\\rho{{=}}{sp}$ with reference "
        f"chrF++, and agrees with the oracle pick {agree}\\% of the time (chance "
        f"{rand}\\%), while {headroom}\\% of the headroom lies in non-top beams.}}\n"
        "\\label{tab:rerank}\n\\end{table}\n"
    )


def main() -> None:
    main_data, ifs_data, corr_data = load(MAIN), load(IFS), load(CORR)
    rerank_data = load(RERANK)
    write(os.path.join(TAB, "main_results.tex"), main_table(main_data, ifs_data))
    if any(k in main_data for k in ("nllb_zeroshot_flores", "nllb_finetuned_flores")):
        write(os.path.join(TAB, "flores_domain.tex"), flores_table(main_data))
    if ifs_data:
        write(os.path.join(TAB, "ifs_components.tex"), ifs_table(ifs_data))
    if corr_data:
        write(os.path.join(TAB, "ifs_correlation.tex"), corr_table(corr_data))
    if rerank_data.get("variants_test"):
        write(os.path.join(TAB, "rerank.tex"), rerank_table(rerank_data, main_data))
    print("tables generated.")


if __name__ == "__main__":
    main()
